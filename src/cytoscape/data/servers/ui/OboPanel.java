	protected void selectButtonActionPerformed() {
		// TODO add your handling code here:
		CyFileFilter oboFilter = new CyFileFilter();
		oboFilter.addExtension("obo");
		oboFilter.setDescription("OBO files");

		// Get the file name
		File originalFile = oboFile;
		oboFile = FileUtil.getFile("Import OBO File", FileUtil.LOAD,
				new CyFileFilter[] { oboFilter });

		if (oboFile != null && oboFile.canRead() == true) {
			this.oboFileNameTextField.setText(oboFile.getAbsolutePath());
			this.oboFileNameTextField.setToolTipText(oboFile.getAbsolutePath());

			try {
				BufferedReader br = new BufferedReader(new FileReader(oboFile));
				headerEditorPane.setText(getHeader(br));
			} catch (IOException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}

		} else if (originalFile == null || originalFile.canRead() == false) {
			if (oboFile != null) {
				oboFileNameTextField.setText(oboFile.getAbsolutePath());
			}
			headerEditorPane.setText("This is not a valid OBO file!!");
		}
	}


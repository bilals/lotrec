	private String getHeader(BufferedReader br) throws IOException {
		String line;
		String header = "";

		while ((line = br.readLine()) != null) {
			if (!(line.startsWith("[Term]"))) {
				header = header + line + LS;
			} else {
				break;
			}
		}

		return header.trim();
	}

